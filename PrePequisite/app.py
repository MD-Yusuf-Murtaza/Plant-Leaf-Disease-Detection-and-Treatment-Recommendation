#  SECTION 6 — SHOW TREATMENT
# ═════════════════════════════════════════════════════════════════════════════

def show_treatment(final_class: Optional[str], language: str):
    if not final_class:
        return "No confirmed detection to look up treatment for.", None

    disease_id = disease_id_mapping.get(final_class)

    # Healthy plant — no treatment
    info = disease_info.get(final_class, {})
    if info.get("is_healthy"):
        t   = ui_translations.get(language, ui_translations["English"])
        msg = t.get("healthy_message", "This plant appears healthy. No treatment needed!")
        return msg, _synthesize_speech(msg, language)

    if not disease_id or disease_id not in TREATMENT_DB:
        return (
            f"⚠️ Treatment protocol not found for `{final_class}` in local database. "
            f"Please use the symptom-based search below.",
            None
        )

    record   = TREATMENT_DB[disease_id]
    treat_md = _format_treatment(record, language, disease_id=disease_id)
    audio    = _synthesize_speech(_plain_text(treat_md), language)
    return treat_md, audio


