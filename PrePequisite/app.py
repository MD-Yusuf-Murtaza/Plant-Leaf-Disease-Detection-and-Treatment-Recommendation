def _get_translated_record(disease_id: str, lang: str) -> dict | None:
    """
    Return the pre-translated record from TRANSLATED_DB cache if available,
    otherwise return None (caller must fall back to live IndicTrans2).
    """
    if _CACHE_LOADED and lang in TRANSLATED_DB:
        return TRANSLATED_DB[lang].get(disease_id)
    return None


def _format_treatment(record: dict, lang: str, disease_id: str = "") -> str:
    """
    Build a plain treatment report from a TREATMENT_DB record.
    Shows only disease/crop name and treatment fields — no bold, no symptoms/pathogen.

    Priority:
      1. TRANSLATED_DB cache (instant, no IndicTrans2 call)
      2. Live IndicTrans2 translation (slow, only if cache absent)
    """
    disease_name = record["disease"]["common_name"]
    crop_name    = record["crop"]["common_name"]

    d_label  = _xt(lang, "disease_label")
    c_label  = _xt(lang, "crop_label")
    chem_lbl = _xt(lang, "chemical_mgmt")
    prev_lbl = _xt(lang, "preventive_measures")

    header = f"{d_label}: {disease_name}  \n{c_label}: {crop_name}\n\n---\n\n"

    if lang == "English":
        chem_list = record["management"]["chemical"]
        prev_list = record["management"]["preventive"]
        body = (
            f"{chem_lbl}:\n\n"
            + "".join(f"- {c}\n" for c in chem_list)
            + f"\n\n{prev_lbl}:\n\n"
            + "".join(f"- {p}\n" for p in prev_list)
        )
        return header + body

    # ── Try pre-translated cache first ───────────────────────────────────────
    cached = _get_translated_record(disease_id, lang) if disease_id else None
    if cached:
        chem_list = cached["management"]["chemical"]
        prev_list = cached["management"]["preventive"]
        body = (
            f"{chem_lbl}:\n\n"
            + "".join(f"- {c}\n" for c in chem_list)
            + f"\n\n{prev_lbl}:\n\n"
            + "".join(f"- {p}\n" for p in prev_list)
        )
        return header + body

    # ── Fallback: live IndicTrans2 translation ────────────────────────────────
    chem_list = record["management"]["chemical"]
    prev_list = record["management"]["preventive"]
    body_en = (
        f"Chemical Management:\n\n"
        + "".join(f"- {c}\n" for c in chem_list)
        + f"\n\nPreventive Measures:\n\n"
        + "".join(f"- {p}\n" for p in prev_list)
    )
    translated = _translate_text(body_en, lang)
    translated = translated.replace("Chemical Management:", f"{chem_lbl}:")
    translated = translated.replace("Preventive Measures:", f"{prev_lbl}:")
    return header + translated


def _format_disease_details(yolo_class: str, lang: str) -> str:
    """Return a Markdown card with taxonomy + symptom info for the detected class."""
    info = disease_info.get(yolo_class, {})
    if not info:
        return "_No detailed taxonomy found for this class._"

    sci_name    = info.get("scientific_name", "N/A")
    common_name = info.get(lang, info.get("English", "N/A"))
    is_healthy  = info.get("is_healthy", False)

    t = ui_translations.get(lang, ui_translations["English"])

    if is_healthy:
        return (
            f"✅ **{t['healthy_message']}**\n\n"
            f"- **{t['scientific_name']}:** *{sci_name}*\n"
            f"- **{t['common_name']}:** {common_name}\n"
        )

    # Get disease_id for richer info
    disease_id  = disease_id_mapping.get(yolo_class)
    record      = TREATMENT_DB.get(disease_id, {}) if disease_id else {}
    pathogen_en = record.get("pathogen_type", "")
    symptoms_en = record.get("symptoms", "Symptom details not available.")

    # Use pre-translated cache when available (avoids IndicTrans2 call)
    cached = _get_translated_record(disease_id, lang) if (disease_id and lang != "English") else None
    if cached:
        pathogen = cached.get("pathogen_type", pathogen_en)
        symptoms = cached.get("symptoms", symptoms_en)
    elif lang != "English":
        pathogen = _translate_text(pathogen_en, lang)
        symptoms = _translate_text(symptoms_en, lang)
    else:
        pathogen = pathogen_en
        symptoms = symptoms_en

    return (
        f"### 🔬 {t['final_analysis']}\n\n"
        f"| {_xt(lang, 'field')} | {_xt(lang, 'detail')} |\n"
        f"|-------|--------|\n"
        f"| {t['scientific_name']} | {sci_name} |\n"
        f"| {t['common_name']} | {common_name} |\n"
        f"| {_xt(lang, 'pathogen_type')} | {pathogen} |\n\n"
        f"**{_xt(lang, 'symptoms')}:**\n\n{symptoms}"
    )


