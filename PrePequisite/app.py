def show_disease_details(final_class: Optional[str], language: str):
    if not final_class:
        return "No confirmed detection to display details for.", None
    details_md  = _format_disease_details(final_class, language)
    audio_path  = _synthesize_speech(_plain_text(details_md), language)
    return details_md, audio_path


def _plain_text(md: str) -> str:
    """Strip Markdown for TTS."""
    import re
    text = re.sub(r"\*\*(.+?)\*\*", r"\1", md)
    text = re.sub(r"\*(.+?)\*",     r"\1", text)
    text = re.sub(r"`(.+?)`",       r"\1", text)
    text = re.sub(r"#+\s*",         "",    text)
    text = re.sub(r"\|.*?\|",       " ",   text)
    text = re.sub(r"-{2,}",         "",    text)
    text = re.sub(r"\n{2,}",        "\n",  text)
    return text.strip()


