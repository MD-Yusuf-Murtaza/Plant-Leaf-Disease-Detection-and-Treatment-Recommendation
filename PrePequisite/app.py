def _get_best_prediction(results) -> Tuple[str, float]:
    """Extract the highest-confidence class from a YOLO result object."""
    # Classification head (probs)
    if hasattr(results, "probs") and results.probs is not None:
        cls_id = results.probs.top1
        conf   = results.probs.top1conf.item() * 100
        return CLASS_NAMES.get(cls_id, "Unknown"), round(conf, 1)
    # Detection head (boxes)
    if results.boxes and len(results.boxes) > 0:
        best_idx = results.boxes.conf.argmax()
        conf     = results.boxes.conf[best_idx].item() * 100
        cls_id   = int(results.boxes.cls[best_idx].item())
        return CLASS_NAMES.get(cls_id, "Unknown"), round(conf, 1)
    return "No Detection", 0.0


def _resolve_final_prediction(
    cls_v8: str, conf_v8: float,
    cls_v12: str, conf_v12: float,
) -> Tuple[Optional[str], float, str]:
    """
    Apply the ensemble decision logic.
    Returns (final_class | None, best_confidence, status_message).
    """
    HIGH = CONFIDENCE_THRESHOLD

    # Both confident & agree
    if conf_v8 >= HIGH and conf_v12 >= HIGH and cls_v8 == cls_v12:
        return cls_v8, max(conf_v8, conf_v12), "agree_high"

    # Both confident & disagree — take the higher one
    if conf_v8 >= HIGH and conf_v12 >= HIGH and cls_v8 != cls_v12:
        winner = (cls_v8, conf_v8) if conf_v8 >= conf_v12 else (cls_v12, conf_v12)
        return winner[0], winner[1], "disagree_high"

    # Only v8 confident
    if conf_v8 >= HIGH:
        return cls_v8, conf_v8, "single_v8"

    # Only v12 confident
    if conf_v12 >= HIGH:
        return cls_v12, conf_v12, "single_v12"

    # Neither confident
    return None, max(conf_v8, conf_v12), "low_confidence"


def _translate_text(english_text: str, target_lang: str) -> str:
    """
    Translate English text to target_lang using IndicTrans2.
    Falls back to English on any error.
    """
    if target_lang == "English":
        return english_text
    lang_code = INDIC_LANG_CODES.get(target_lang)
    if not lang_code:
        return english_text

    if not _load_indic_model():
        return f"[Translation unavailable — IndicTrans2 not loaded]\n\n{english_text}"

    try:
        batch  = _indic_processor.preprocess_batch(
            [english_text], src_lang="eng_Latn", tgt_lang=lang_code
        )
        inputs = _indic_tokenizer(
            batch, truncation=True, padding="longest",
            return_tensors="pt", return_attention_mask=True
        ).to(DEVICE)
        with torch.no_grad():
            generated = _indic_model.generate(
                **inputs, use_cache=False, min_length=0, max_length=512, num_beams=5
            )
        with _indic_tokenizer.as_target_tokenizer():
            decoded = _indic_tokenizer.batch_decode(
                generated.detach().cpu().tolist(),
                skip_special_tokens=True,
                clean_up_tokenization_spaces=True,
            )
        return _indic_processor.postprocess_batch(decoded, lang=lang_code)[0]
    except Exception as e:
        log.error(f"Translation error: {e}")
        return english_text


def _synthesize_speech(text: str, lang: str) -> Optional[str]:
    """
    Convert text to an MP3 file using edge-tts via subprocess.
    Returns the path to a temporary .mp3 file, or None on failure.
    """
    voice = TTS_VOICES.get(lang, "en-US-JennyNeural")
    try:
        tmp = tempfile.NamedTemporaryFile(suffix=".mp3", delete=False)
        tmp.close()
        result = subprocess.run(
            ["edge-tts", "--voice", voice, "--text", text[:1500], "--write-media", tmp.name],
            capture_output=True, timeout=30
        )
        if result.returncode == 0 and Path(tmp.name).stat().st_size > 0:
            return tmp.name
        log.error(f"edge-tts error: {result.stderr.decode()[:200]}")
        return None
    except Exception as e:
        log.error(f"TTS failed: {e}")
        return None


