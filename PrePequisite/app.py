#  SECTION 7 — SYMPTOM-BASED FALLBACK (RAG)
# ═════════════════════════════════════════════════════════════════════════════

def search_by_symptoms(crop_name: str, symptoms_text: str, language: str):
    if not crop_name.strip() or not symptoms_text.strip():
        return "⚠️ Please enter both a crop name and a symptom description.", None

    if not _load_rag_chain():
        # Attempt local keyword matching as a fallback
        return _local_symptom_match(crop_name, symptoms_text, language)

    try:
        english_result = _rag_chain.invoke({
            "crop": crop_name,
            "symptoms": symptoms_text,
        })
    except Exception as e:
        log.error(f"RAG chain error: {e}")
        return _local_symptom_match(crop_name, symptoms_text, language)

    # Strip any residual markdown Gemini added (bold, italic, headers)
    import re
    english_clean = re.sub(r"\*{1,3}(.+?)\*{1,3}", r"\1", english_result)
    english_clean = re.sub(r"^#{1,3}\s*", "", english_clean, flags=re.MULTILINE)
    english_clean = re.sub(r"^\d+\.\s*", "- ", english_clean, flags=re.MULTILINE)

    # Translate output if needed
    final_text = _translate_text(english_clean, language) if language != "English" else english_clean
    audio      = _synthesize_speech(_plain_text(final_text), language)
    return final_text, audio


def _local_symptom_match(crop_name: str, symptoms_text: str, language: str) -> Tuple[str, None]:
    """
    Keyword-based fallback when Gemini RAG is unavailable.
    Accepts input in any language — translates to English first for keyword matching.
    """
    # Translate user input to English for matching if not already English
    query_en = _translate_text(symptoms_text, "English") if language != "English" else symptoms_text
    # Also keep original words as fallback (catches Hinglish / English mixed input)
    query_words = set(query_en.lower().split()) | set(symptoms_text.lower().split())
    crop_lower  = crop_name.lower()
    best_match  = None
    best_score  = 0

    best_disease_id = ""
    for disease_id, record in TREATMENT_DB.items():
        crop_match = crop_lower in record["crop"]["common_name"].lower()
        if not crop_match:
            continue
        symptom_words = set(record.get("symptoms", "").lower().split())
        score = len(query_words & symptom_words)
        if score > best_score:
            best_score      = score
            best_match      = record
            best_disease_id = disease_id

    if best_match:
        label = _xt(language, "best_local_match")
        treatment = _format_treatment(best_match, language, disease_id=best_disease_id)
        result = f"{label}\n\n{treatment}"
    else:
        result = _xt(language, "no_match")
    audio = _synthesize_speech(_plain_text(result), language)
    return result, audio


