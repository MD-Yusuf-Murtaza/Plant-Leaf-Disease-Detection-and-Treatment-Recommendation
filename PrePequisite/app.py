def run_prediction(image: Image.Image, language: str):
    """
    Main pipeline:
    - Load YOLO models (if needed)
    - Resize image to 640×640
    - Run both models and apply ensemble decision logic
    - Return: (annotated_v8, annotated_v12, raw_md, status, final_class, is_low_conf)
    """
    if image is None:
        return None, None, "⚠️ Please upload an image first.", "idle", None, False

    t = ui_translations.get(language, ui_translations["English"])

    if not _load_yolo_models():
        return (
            None, None,
            "❌ YOLO models could not be loaded. Please check model paths in Space settings.",
            "error", None, False
        )

    img_resized = image.resize((640, 640))

    # Run inference
    res_v8  = _yolo_v8(img_resized)[0]
    res_v12 = _yolo_v12(img_resized)[0]

    cls_v8,  conf_v8  = _get_best_prediction(res_v8)
    cls_v12, conf_v12 = _get_best_prediction(res_v12)

    # Annotated images (BGR → RGB)
    try:
        img_ann_v8  = Image.fromarray(res_v8.plot()[..., ::-1])
        img_ann_v12 = Image.fromarray(res_v12.plot()[..., ::-1])
    except Exception:
        img_ann_v8  = img_resized
        img_ann_v12 = img_resized

    # Ensemble decision
    final_class, best_conf, status = _resolve_final_prediction(
        cls_v8, conf_v8, cls_v12, conf_v12
    )

    raw_md = _build_raw_predictions_md(cls_v8, conf_v8, cls_v12, conf_v12, language)

    # ── Status banner ──────────────────────────────────────────────────────
    if status == "agree_high":
        banner = (
            f"✅ **High Confidence Agreement** — Both models agree: "
            f"**{final_class}** ({best_conf:.1f}%)"
        )
    elif status == "disagree_high":
        banner = (
            f"⚠️ **Models Disagree** — Both confident but differ. "
            f"Using higher-confidence result: **{final_class}** ({best_conf:.1f}%). "
            f"Manual verification recommended."
        )
    elif status == "single_v8":
        banner = (
            f"🔍 **Single Model Result** — YOLOv8n is confident: "
            f"**{final_class}** ({best_conf:.1f}%). YOLOv12n was uncertain."
        )
    elif status == "single_v12":
        banner = (
            f"🔍 **Single Model Result** — YOLOv12n is confident: "
            f"**{final_class}** ({best_conf:.1f}%). YOLOv8n was uncertain."
        )
    else:  # low_confidence
        banner = (
            f"❌ **Low Confidence** — Neither model exceeded {CONFIDENCE_THRESHOLD}%. "
            f"Best guesses: {cls_v8} ({conf_v8:.1f}%) & {cls_v12} ({conf_v12:.1f}%). "
            f"Please describe symptoms below for AI-assisted diagnosis."
        )

    is_low_conf = (final_class is None)
    return img_ann_v8, img_ann_v12, raw_md + "\n" + banner, status, final_class, is_low_conf


