def _xt(lang: str, key: str) -> str:
    """Get extra translation key, fallback to English."""
    return EXTRA_T.get(lang, EXTRA_T["English"]).get(key, EXTRA_T["English"][key])

# ─── YOLO loader ─────────────────────────────────────────────────────────────
def _load_yolo_models():
    global _yolo_v8, _yolo_v12
    if _yolo_v8 is not None:
        return True
    try:
        from ultralytics import YOLO
        v8_path  = os.environ.get("YOLO_V8_PATH",  "models/finetuned_yolo8n.pt")
        v12_path = os.environ.get("YOLO_V12_PATH", "models/finetuned_yolo12n.pt")

        if not Path(v8_path).exists():
            log.warning(f"YOLOv8 weights not found at {v8_path}. Using base yolov8n.")
            v8_path = "yolov8n.pt"
        if not Path(v12_path).exists():
            log.warning(f"YOLOv12 weights not found at {v12_path}. Using base yolov8n as fallback.")
            v12_path = v8_path

        _yolo_v8  = YOLO(v8_path)
        _yolo_v12 = YOLO(v12_path)
        log.info("✅ YOLO models loaded successfully.")
        return True
    except Exception as e:
        log.error(f"❌ YOLO load failed: {e}")
        return False


# ─── IndicTrans2 loader ───────────────────────────────────────────────────────
def _load_indic_model():
    global _indic_tokenizer, _indic_model, _indic_processor
    if _indic_tokenizer is not None:
        return True
    try:
        # Install IndicTransToolkit at runtime to avoid HF Spaces init-phase
        # DNS failures that occur when pip tries to clone git packages during build.
        import importlib, subprocess, sys
        if importlib.util.find_spec("IndicTransToolkit") is None:
            log.info("⏳ Installing IndicTransToolkit (first run only)…")
            subprocess.check_call([
                sys.executable, "-m", "pip", "install", "--quiet",
                "git+https://github.com/VarunGumma/IndicTransToolkit.git"
            ])
            log.info("✅ IndicTransToolkit installed.")

        from transformers import AutoModelForSeq2SeqLM, AutoTokenizer
        from IndicTransToolkit import IndicProcessor

        model_name = "ai4bharat/indictrans2-en-indic-dist-200M"
        log.info("⏳ Loading IndicTrans2 model (first run may take ~2 min)…")
        _indic_tokenizer  = AutoTokenizer.from_pretrained(
            model_name, trust_remote_code=True, token=HF_TOKEN or True
        )
        _indic_model = AutoModelForSeq2SeqLM.from_pretrained(
            model_name, trust_remote_code=True, token=HF_TOKEN or True
        ).to(DEVICE)
        _indic_processor = IndicProcessor(inference=True)
        log.info("✅ IndicTrans2 loaded.")
        return True
    except Exception as e:
        log.error(f"❌ IndicTrans2 load failed: {e}")
        return False


